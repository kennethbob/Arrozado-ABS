$(document).ready(function(){

	$("#login").click(function(){
		$(".login").fadeIn(2000);
		$(".registration").hide();
	});
	$("#registration").click(function(){
		$(".registration").fadeIn(2000);
		$(".login").hide();
	});
	$("#collect").mouseover(function(){
		$(".collections").show();
	});
	$("#collect").mouseclick(function(){
		$(".collections").hide();
	});

});
